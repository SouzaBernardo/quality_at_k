        # convert each part to the corresponding integer
        intparts = []
        for part in parts:
            if part in self.ordinal_words:
                intparts.append(str(self.ordinal_words[part]))
            elif part!= '':
                intparts.append(str(self.numwords[part][0]))
                if self.numwords[part][1]:
                    intparts.append(str(self.numwords[part][1]))
        # join the integer parts and return
        return ''.join(intparts)

